-- 1. Create CUSTOMER table with proper normalization
CREATE TABLE CUSTOMER (
    CUST_NO NUMBER(4) PRIMARY KEY,
    FIRST_NAME VARCHAR2(20) NOT NULL,
    LAST_NAME VARCHAR2(20),
    ADDRESS VARCHAR2(20),
    CITY VARCHAR2(8),
    STATE VARCHAR2(20),
    PIN NUMBER(6),
    B_DATE DATE,
    STATUS CHAR(1) CHECK (STATUS IN ('V','I','A'))
);

